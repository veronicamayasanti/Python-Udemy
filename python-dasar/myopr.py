# operator assignment (penugasan)
x = 10
print("x awal:", x)
x += 5 # sama dengan x = x + 5
print("x setelah x +=5 : ", x)
x -= 3 # sama dengan x = x -3
print(" x setelah -= 3 : ", x)
x *= 2 # sama dengan : x = x*2
print("setelah *=2: ", x)
x /= 4
print("x setelah /= 4: ", x)

# operator string
